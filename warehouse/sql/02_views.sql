-- Consumer-facing secure views in SHARE (also useful in CURATED for ops).
USE DATABASE AI_PRICE_INTEL;

-- Current-price convenience views (CURATED, non-secure for provider ops)
CREATE OR REPLACE VIEW CURATED.VW_MODEL_CURRENT AS
SELECT
  m.MODEL_ID,
  m.PROVIDER_ID,
  p.NAME AS PROVIDER_NAME,
  m.MODEL_NAME,
  m.FAMILY,
  m.MODALITY,
  m.CONTEXT_WINDOW_TOKENS,
  m.MAX_OUTPUT_TOKENS,
  m.SUPPORTS_TOOLS,
  m.SUPPORTS_VISION,
  m.LICENSE,
  m.IS_ACTIVE,
  h.PRICE_INPUT_USD_PER_1M_TOKENS,
  h.PRICE_OUTPUT_USD_PER_1M_TOKENS,
  h.PRICE_CACHED_INPUT_USD_PER_1M_TOKENS,
  h.BATCH_DISCOUNT_PCT,
  h.TIER_NOTES,
  h.VALID_FROM,
  h.SOURCE_URL,
  h.RETRIEVED_AT
FROM CURATED.DIM_MODEL m
JOIN CURATED.DIM_PROVIDER p ON p.PROVIDER_ID = m.PROVIDER_ID
JOIN CURATED.FACT_MODEL_PRICE_HISTORY h
  ON h.MODEL_ID = m.MODEL_ID AND h.IS_CURRENT = TRUE;

CREATE OR REPLACE VIEW CURATED.VW_GPU_CURRENT AS
