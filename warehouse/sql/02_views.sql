  g.CLOUD,
  g.INSTANCE_NAME,
  g.GPU_MODEL,
  g.GPU_COUNT,
  g.VRAM_GB,
  h.REGION,
  h.PRICE_ON_DEMAND_USD_HR,
  h.PRICE_SPOT_USD_HR,
  h.PRICE_1YR_RESERVED_USD_HR,
  h.VALID_FROM,
