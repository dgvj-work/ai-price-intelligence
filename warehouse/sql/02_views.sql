CREATE OR REPLACE VIEW CURATED.VW_CORTEX_CURRENT AS
SELECT
  FUNCTION_NAME,
  MODEL_NAME,
  CREDITS_PER_1M_TOKENS,
  VALID_FROM,
  SOURCE_URL,
  RETRIEVED_AT
FROM CURATED.FACT_CORTEX_PRICE_HISTORY
WHERE IS_CURRENT = TRUE;

CREATE OR REPLACE VIEW CURATED.VW_PRICE_CHANGES_90D AS
WITH ordered AS (
  SELECT
    MODEL_ID,
    PRICE_INPUT_USD_PER_1M_TOKENS,
    PRICE_OUTPUT_USD_PER_1M_TOKENS,
    VALID_FROM,
    VALID_TO,
    IS_CURRENT,
    SOURCE_URL,
    LAG(PRICE_INPUT_USD_PER_1M_TOKENS) OVER (PARTITION BY MODEL_ID ORDER BY VALID_FROM) AS PREV_INPUT,
    LAG(PRICE_OUTPUT_USD_PER_1M_TOKENS) OVER (PARTITION BY MODEL_ID ORDER BY VALID_FROM) AS PREV_OUTPUT
  FROM CURATED.FACT_MODEL_PRICE_HISTORY
)
SELECT
  MODEL_ID,
  VALID_FROM AS CHANGED_AT,
  PREV_INPUT AS OLD_INPUT_USD_PER_1M,
  PRICE_INPUT_USD_PER_1M_TOKENS AS NEW_INPUT_USD_PER_1M,
  CASE
    WHEN PREV_INPUT IS NULL OR PREV_INPUT = 0 THEN NULL
    ELSE ROUND(100 * (PRICE_INPUT_USD_PER_1M_TOKENS - PREV_INPUT) / PREV_INPUT, 2)
  END AS INPUT_PCT_CHANGE,
  PREV_OUTPUT AS OLD_OUTPUT_USD_PER_1M,
  PRICE_OUTPUT_USD_PER_1M_TOKENS AS NEW_OUTPUT_USD_PER_1M,
  CASE
    WHEN PREV_OUTPUT IS NULL OR PREV_OUTPUT = 0 THEN NULL
    ELSE ROUND(100 * (PRICE_OUTPUT_USD_PER_1M_TOKENS - PREV_OUTPUT) / PREV_OUTPUT, 2)
  END AS OUTPUT_PCT_CHANGE,
  SOURCE_URL
FROM ordered
WHERE PREV_INPUT IS NOT NULL
  AND VALID_FROM >= DATEADD('day', -90, CURRENT_TIMESTAMP());

CREATE OR REPLACE VIEW CURATED.VW_COST_PER_BENCHMARK_POINT AS
SELECT
  c.MODEL_ID,
  c.MODEL_NAME,
  c.PROVIDER_NAME,
  c.PRICE_INPUT_USD_PER_1M_TOKENS,
  c.PRICE_OUTPUT_USD_PER_1M_TOKENS,
  b.BENCHMARK_NAME,
  b.SCORE,
  b.SCORE_TYPE,
  CASE
    WHEN b.SCORE IS NULL OR b.SCORE = 0 THEN NULL
    ELSE ROUND(c.PRICE_INPUT_USD_PER_1M_TOKENS / b.SCORE, 6)
  END AS INPUT_USD_PER_BENCHMARK_POINT,
  CASE
    WHEN b.SCORE IS NULL OR b.SCORE = 0 THEN NULL
    ELSE ROUND(
      (COALESCE(c.PRICE_INPUT_USD_PER_1M_TOKENS, 0) + COALESCE(c.PRICE_OUTPUT_USD_PER_1M_TOKENS, 0))
        / b.SCORE,
      6
    )
  END AS BLENDED_USD_PER_BENCHMARK_POINT
FROM CURATED.VW_MODEL_CURRENT c
JOIN CURATED.FACT_BENCHMARK_SCORE b
  ON b.MODEL_ID = c.MODEL_ID
 AND UPPER(b.BENCHMARK_NAME) = 'MMLU';

